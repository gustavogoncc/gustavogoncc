public class Livro {
	  private String nome;
	  private int numeroDePaginas;
	  
	  public Livro(String nome, int numeroDePaginas) {
		  this.nome = nome;
		  this.numeroDePaginas = numeroDePaginas;
	  }
	  public String getNome() {
		  return nome;
	  }
	  public int getNumeroDePaginas() {
		  return numeroDePaginas;
	  }
	   
}
